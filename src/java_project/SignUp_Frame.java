    private void subtn_signupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_subtn_signupActionPerformed
        
        if(signup_checkInput() == true)
        {
            
            try {
                Connection con = getConnection();
                PreparedStatement ps = con.prepareStatement("INSERT into Users (Name,User_Id,Password,User_Type) values (?,?,?,?)");
                ps.setString(1, sutxt_name.getText());
                ps.setString(2, sutxt_userid.getText());
                ps.setString(3, sutxt_pass.getText());
                ps.setString(4, (String)cobox_UserType.getSelectedItem());
                ps.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "data");
            } catch (SQLException ex) {
                Logger.getLogger(SignUp_Frame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"One or More Fields are missing");
        }
        System.out.println("name :" + sutxt_name.getText());
        System.out.println("userid :" + sutxt_userid.getText());
        System.out.println("password :" + sutxt_pass.getText());
        System.out.println("confirm password :" + sutxt_copass.getText());
    }//GEN-LAST:event_subtn_signupActionPerformed

