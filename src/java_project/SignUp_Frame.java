    public boolean signup_checkInput()
    {
        String ps1 = new String();
        String ps2 = new String();
        ps1 = sutxt_pass.getText();
        ps2 = sutxt_copass.getText();
        int compareTo = ps1.compareTo(ps2);
        
                
        if( sutxt_name.getText() == null
                || sutxt_userid.getText() == null
                || compareTo == 1
                )
        {
            return false;
        }
        else
        {
            return true;
        }
    }
